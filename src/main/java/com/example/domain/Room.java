package com.example.domain;

public class Room {
    private String name;

    private Room(){

    }
    public Room(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
